    def self.decode(encoded_list)
      decoded_list = {}
      lines = Zlib::Inflate.inflate(Base64.decode64(encoded_list)).split(/\n/)
      lines.grep(/^\d{5}/).each do |line|
        key, value = line.chomp.split
        decoded_list[key.to_sym] = value
      end
      decoded_list
    end

